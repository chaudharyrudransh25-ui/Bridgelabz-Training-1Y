class assignment
{
	public static void main(String args[])
	{
		int a = 10;
		    a += 5;
			System.out.println("Sum : " + a);
			
			a -= 5;
			System.out.println("Difference : " + a);
			
			a *= 5;
			System.out.println("multiplication : " + a);

			a /= 5;
			System.out.println("division : " + a);
			
			a %= 5;
			System.out.println("remainder : " + a);
		
		
	}
}